// Example
// Input : Number1 = 12 , Number2 = 15
// Output : 54

// Find the Sum of the Numbers in a Given Range in Javascript
// Given two integer inputs number1 and number2, the objective is to find the sum of all Number that lay in the given interval by writing a code to Find the Sum of the Numbers in a Given Range in Javascript Language.


let number1=12
let number2=15

let sum=0
for(let i=number2;i>=number1;i--){

    sum=sum+i;
}
console.log(sum)


